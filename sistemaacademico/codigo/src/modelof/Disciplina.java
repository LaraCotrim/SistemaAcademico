package modelof;

public class Disciplina {
	
	private int iddis;
	private String nomed;
	private String cursodis;
	private String professor;
	
	
	
	public int getIddis() {
		return iddis;
	}
	public void setIddis(int iddis) {
		this.iddis = iddis;
	}
	public String getNomed() {
		return nomed;
	}
	public void setNomed(String nomed) {
		this.nomed = nomed;
	}
	public String getCursodis() {
		return cursodis;
	}
	public void setCursodis(String cursodis) {
		this.cursodis = cursodis;
	}
	public String getProfessor() {
		return professor;
	}
	public void setProfessor(String professor) {
		this.professor = professor;
	}
	
	

}
